### Compare Git + Mercurial ###

# As of 9/19/2015:
# This is an excellent Git tutorial: http://rogerdudler.github.io/git-guide/
# This is an excellent Mercurial tutorial: http://hginit.com/
# And this is a slightly dated, but solid comparison: https://importantshock.wordpress.com/2008/08/07/git-vs-mercurial/

# Summary:
# Git pros: Flexible, speedy, scales
# Git cons: Complex, esoteric
# Mercurial pros: Simple, works-out-of-the-box
# Mercurial concs: Seemingly inflexible, doesn't scale well
# The average person working on simple projects, don't care about the slight differences. Use whatever you prefer.
# For a larger org with many developers and projects, Git is more effective.
